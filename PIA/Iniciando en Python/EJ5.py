# 1. Declarar las variables
x = True
y = False
z = True

# 2. Evaluar las expresiones lógicas y mostrar si son True o False
print("x and y:", x and y)         # Evaluación de x and y
print("x or y:", x or y)           # Evaluación de x or y
print("not x:", not x)             # Evaluación de not x
print("(x or y) and z:", (x or y) and z)  # Evaluación de (x or y) and z
print("x == z:", x == z)           # Evaluación de x == z

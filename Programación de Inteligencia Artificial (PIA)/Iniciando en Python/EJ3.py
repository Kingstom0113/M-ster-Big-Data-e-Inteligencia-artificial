# 1. Declarar la lista 'números'
numeros = [3, 8, 1, 6, 0, 8, 4]

# 2. Agregar un nuevo número al final de la lista (elegimos el número 10)
numeros.append(10)

# 3. Eliminar el primer elemento de la lista
numeros.pop(0)

# 4. Ordenar la lista de menor a mayor
numeros.sort()

# 5. Imprimir la lista y su longitud
print("Lista ordenada:", numeros)
print("Longitud de la lista:", len(numeros))

# Declaración de variables de tipo cadena
nombre = "Juan"
apellido = "Pérez"

# Concatenación de las cadenas
nombre_completo = nombre + " " + apellido

# Imprimir la longitud de la cadena nombre_completo
print(f"Nombre completo: {nombre_completo}")
print(f"Longitud de la cadena: {len(nombre_completo)}")

# Convertir a mayúsculas y minúsculas
nombre_mayusculas = nombre_completo.upper()
nombre_minusculas = nombre_completo.lower()

# Imprimir los resultados
print(f"Nombre completo en mayúsculas: {nombre_mayusculas}")
print(f"Nombre completo en minúsculas: {nombre_minusculas}")

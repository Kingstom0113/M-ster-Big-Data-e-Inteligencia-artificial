# 1. Crear la lista 'colores' con los valores iniciales
colores = ["rojo", "verde", "azul", "amarillo"]

# 2. Agregar el color "morado" al final de la lista
colores.append("morado")

# 3. Insertar el color "naranja" en la segunda posición
colores.insert(1, "naranja")

# 4. Eliminar el color "verde" de la lista
colores.remove("verde")

# 5. Ordenar la lista alfabéticamente
colores.sort()

# 6. Invertir el orden de los elementos de la lista
colores.reverse()

# 7. Imprimir la lista final
print("Lista final:", colores)
